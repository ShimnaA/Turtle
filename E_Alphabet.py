# Draw E alphabet using turtle

import turtle

tt = turtle.Turtle()
tt.penup()
tt.setpos(-40,80)
tt.color("brown")
tt.pensize(10)
tt.pendown()
tt.forward(100)
tt.backward(100)
tt.right(90)
tt.forward(100)
tt.left(90)
tt.forward(100)
tt.backward(100)
tt.right(90)
tt.forward(100)
tt.left(90)
tt.forward(100)

turtle.done()

